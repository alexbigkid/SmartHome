  `HostCountry1Id` SMALLINT UNSIGNED NOT NULL,
  `HostCountry2Id` SMALLINT UNSIGNED NULL DEFAULT NULL,
  `HostCountry3Id` SMALLINT UNSIGNED NULL DEFAULT NULL,
  `1stPlacedCountryId` SMALLINT UNSIGNED NOT NULL,
  `2ndPlacedCountryId` SMALLINT UNSIGNED NOT NULL,
  `3rdPlacedCountryId` SMALLINT UNSIGNED NOT NULL,
  `4thPlacedCountryId` SMALLINT UNSIGNED NOT NULL,
  `1stPlacedLastGameScore` TINYINT UNSIGNED NOT NULL,
  `2ndPlacedLastGameScore` TINYINT UNSIGNED NOT NULL,
  `3rdPlacedLastGameScore` TINYINT UNSIGNED NOT NULL,
  `4thPlacedLastGameScore` TINYINT UNSIGNED NOT NULL,
  `numberOfTeams` TINYINT UNSIGNED NOT NULL,
  PRIMARY KEY (`year`),
  KEY `HostCountry1Id` (`HostCountry1Id`),
  KEY `HostCountry2Id` (`HostCountry2Id`),
  KEY `HostCountry3Id` (`HostCountry3Id`),
  KEY `1stPlacedCountryId` (`1stPlacedCountryId`),
