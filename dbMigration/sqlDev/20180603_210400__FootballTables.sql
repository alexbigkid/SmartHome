  KEY `3rdPlacedCountryId` (`3rdPlacedCountryId`),
  KEY `4thPlacedCountryId` (`4thPlacedCountryId`),
  CONSTRAINT `HostCountry1Id` FOREIGN KEY (`HostCountry1Id`) REFERENCES `Country` (`id`),
  CONSTRAINT `HostCountry2Id` FOREIGN KEY (`HostCountry2Id`) REFERENCES `Country` (`id`),
  CONSTRAINT `HostCountry3Id` FOREIGN KEY (`HostCountry3Id`) REFERENCES `Country` (`id`),
  CONSTRAINT `1stPlacedCountryId` FOREIGN KEY (`1stPlacedCountryId`) REFERENCES `Country` (`id`),
  CONSTRAINT `2ndPlacedCountryId` FOREIGN KEY (`2ndPlacedCountryId`) REFERENCES `Country` (`id`),
  CONSTRAINT `3rdPlacedCountryId` FOREIGN KEY (`3rdPlacedCountryId`) REFERENCES `Country` (`id`),
  CONSTRAINT `4thPlacedCountryId` FOREIGN KEY (`4thPlacedCountryId`) REFERENCES `Country` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

